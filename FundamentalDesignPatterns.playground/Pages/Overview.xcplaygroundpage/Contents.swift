 - - - - - - - - - -
 Существует три типа паттернов проектирования:
 
 1. **Structural patterns (_Структурные паттерны_)** описывают, как объекты компонуются для формирования более крупных подсистем.
 Примерами структурных паттернов являются:
 - [Model-view-controller (MVC)](Model-View-Controller)
 
 2. **Behavioral patterns (_Поведенческие паттерны_)** описывают, как объекты взаимодействуют друг с другом.
 Примерами поведенческих паттернов являются:
 - [Delegation](Delegation)
 - [Strategy](Strategy)
 - [Observer](Observer)
 - [Memento](Memento)
 
 3. **Creational patterns (_Креативные паттерны_)** инстанцируют или "создают" объекты для вас.
 Примерами креативных паттернов являются:
 - [Singleton](Singleton)
 - [Builder](Builder)
